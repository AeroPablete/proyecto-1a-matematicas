module.exports = {
    /**
     * Suma de dos números
     * 
     * @example 
     * @param {*} a 
     * @param {*} b 
     */
    suma: function(a,b) {
        return a + b;
    },

    /**
     * Resta de dos números
     * 
     * @param {*} a 
     * @param {*} b 
     */
    resta: function(a,b) {
        return a - b;
    },

    /**
     * Multiplicación de dos números
     * 
     * @param {*} a 
     * @param {*} b 
     */
    multiplicacion: function(a,b) {
        return a * b;
    },

    /**
     * División de dos números
     * 
     * @param {*} a 
     * @param {*} b 
     */
    division: function(a,b) {
        return a / b;
    }
}